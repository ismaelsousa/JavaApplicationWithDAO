    private void newButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newButtonActionPerformed
        if (!this.dataInicioField.getText().equals("") && !this.dataFimField.getText().equals("") && !this.idLocatarioField.getText().equals("") && !this.idLocadorLabel.getText().equals("") && !this.statusField.getText().equals("")) {
            Contrato c = new Contrato();
            Locador loc = new Locador(Integer.parseInt(this.idLocadorField.getText()));
            Locatario loca = new Locatario(Integer.parseInt(this.idLocatarioField.getText()));
            c.setLocador(loc);
            c.setLocatario(loca);
            c.setDataInicio(this.dataInicioField.getText());
            c.setDataFim(this.dataFimField.getText());
            c.setStatus(this.statusField.getText());

            DAOContrato dao = new DAOContrato();
            dao.save(c);
            this.dataInicioField.setText("");
            this.dataFimField.setText("");
            this.statusField.setText("");
            readJTable();
            JOptionPane.showMessageDialog(null, "cadastrado com sucesso");

        } else {
            JOptionPane.showMessageDialog(null, "Por favor preencher todos os campos para realizar o cadastro");
        }
    }//GEN-LAST:event_newButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (this.masterTable.getSelectedRow() != -1) {
            Contrato c = new Contrato();
            //vai pegar o valor da linha selecionada
            c.setId((int) this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 0));
            DAOContrato dao = new DAOContrato();
            dao.delete(c);
            readJTable();
            JOptionPane.showMessageDialog(null, "Excluido com sucesso");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        if (this.masterTable.getSelectedRow() != -1) {
            Contrato c = new Contrato();
            
            //vai pegar o valor da linha selecionada
            c.setId(Integer.parseInt(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 0).toString()));
            c.setDataInicio(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 1).toString());
            c.setDataFim(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 2).toString());
            c.setLocatario(new Locatario(Integer.parseInt(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 3).toString())));
            c.setLocador(new Locador((Integer.parseInt(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 4).toString()))));
            c.setStatus(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 5).toString());

            DAOContrato dao = new DAOContrato();
            dao.update(c);
            readJTable();
            JOptionPane.showMessageDialog(null, "Alterado com sucesso");
        }
    }//GEN-LAST:event_refreshButtonActionPerformed

