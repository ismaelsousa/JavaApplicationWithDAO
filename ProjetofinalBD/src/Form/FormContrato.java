    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) this.masterTable.getModel();
        modelo.setNumRows(0);
        DAOContrato dao = new DAOContrato();
        for (Contrato c : dao.findAll()) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getDataInicio(),
                c.getDataFim(),
                c.getLocatario().getId(),
                c.getLocador().getId(),
                c.getStatus()
            });
        }
    }

