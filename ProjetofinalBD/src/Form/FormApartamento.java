    private void newButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newButtonActionPerformed
        if (!this.qtdQuartoField.getText().equals("") && !this.estadoField.getText().equals("") && !this.idCondominioField.getText().equals("") ) {

            Apartamento ap = new Apartamento();
            Condominio c = new Condominio();
            ap.setEstado(this.estadoField.getText());
            ap.setQtdQuarto(Integer.parseInt(this.qtdQuartoField.getText()));
            c.setId(Integer.parseInt(this.idCondominioField.getText()));
            ap.setCondominio(c);            
            DAOApartamento dao = new DAOApartamento();            
            dao.save(ap);
            this.qtdQuartoField.setText("");
            this.estadoField.setText("");
            this.idCondominioField.setText("");
            readJTable();
            JOptionPane.showMessageDialog(null,"cadastrado com sucesso");

        }else{
            JOptionPane.showMessageDialog(null, "Por favor preencher todos os campos para realizar o cadastro");
        }
    }//GEN-LAST:event_newButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (this.masterTable.getSelectedRow() != -1) {
           Apartamento ap = new Apartamento();
            //vai pegar o valor da linha selecionada
            ap.setId((int) this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 0));

            DAOApartamento dao = new DAOApartamento();
            dao.delete(ap);
            readJTable();
            JOptionPane.showMessageDialog(null,"Excluido com sucesso");            
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        if (this.masterTable.getSelectedRow() != -1) {
            Apartamento ap = new Apartamento();
            //vai pegar o valor da linha selecionada
            ap.setId( Integer.parseInt(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 0).toString()));
            ap.setQtdQuarto(Integer.parseInt(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 1).toString()));
            ap.setEstado(this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 2).toString());
            Condominio c = new Condominio(Integer.parseInt( this.masterTable.getValueAt(this.masterTable.getSelectedRow(), 3).toString()));
            ap.setCondominio(c);
            
            DAOApartamento dao = new DAOApartamento();
            dao.update(ap);
            readJTable();
            JOptionPane.showMessageDialog(null,"Alterado com sucesso");
        }
    }//GEN-LAST:event_refreshButtonActionPerformed

