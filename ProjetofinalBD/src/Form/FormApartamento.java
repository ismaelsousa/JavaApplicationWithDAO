     public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) this.masterTable.getModel();
        modelo.setNumRows(0);
        DAOApartamento dao = new DAOApartamento();
        for (Apartamento ap : dao.findAll()) {
            modelo.addRow(new Object[]{
                ap.getId(),
                ap.getQtdQuarto(),
                ap.getEstado(),
                ap.getCondominio().getId(),});
        }
    }

