    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) this.JTable.getModel();
        modelo.setNumRows(0);
        DAOMensalidade dao = new DAOMensalidade();
        for (Mensalidade m : dao.findAll()) {
            modelo.addRow(new Object[]{
                m.getId(),
                m.getAluguel().getId(),
                m.getValor(),
                m.getStatus(),
                m.getMes()
            });
        }
    }

