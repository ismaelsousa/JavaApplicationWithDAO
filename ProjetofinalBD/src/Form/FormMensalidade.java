    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        // TODO add your handling code here:
        if (!this.idAluguelField.getText().equals("") && !this.valorField.getText().equals("")
                && !this.statusField.getText().equals("") && !this.mesField.getText().equals("")) {

            Aluguel alu = new Aluguel();

            alu.setId(Integer.parseInt(idAluguelField.getText()));

            Mensalidade m = new Mensalidade();
            m.setAluguel(alu);
            m.setMes(mesField.getText());
            m.setStatus(this.statusField.getText());
            m.setValor(Float.parseFloat(valorField.getText()));

            DAOMensalidade dao = new DAOMensalidade();

            dao.save(m);
            mesField.setText("");
            statusField.setText("");
            valorField.setText("");
            idAluguelField.setText("");
            readJTable();
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");

        } else {
            JOptionPane.showMessageDialog(null, "Por favor preencher todos os campos para realizar o cadastro");
        }
    }//GEN-LAST:event_cadastrarActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        // TODO add your handling code here:
        if (this.JTable.getSelectedRow() != -1) {
            Mensalidade m = new Mensalidade();
            //vai pegar o valor da linha selecionada
            m.setId((int) this.JTable.getValueAt(this.JTable.getSelectedRow(), 0));

            DAOMensalidade dao = new DAOMensalidade();

            dao.delete(m);
            readJTable();
            JOptionPane.showMessageDialog(null, "Excluido com sucesso");
        }
    }//GEN-LAST:event_ExcluirActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        // TODO add your handling code here:
        if (this.JTable.getSelectedRow() != -1) {
            Aluguel alu = new Aluguel();
            Mensalidade m = new Mensalidade();
            alu.setId((int) this.JTable.getValueAt(this.JTable.getSelectedRow(), 1));
            m.setAluguel(alu);            
            m.setValor(Float.parseFloat(this.JTable.getValueAt(this.JTable.getSelectedRow(), 2).toString()));
            m.setStatus(this.JTable.getValueAt(this.JTable.getSelectedRow(), 3).toString());
            m.setMes(this.JTable.getValueAt(this.JTable.getSelectedRow(), 4).toString());
            
            
            DAOMensalidade dao = new DAOMensalidade();

            dao.update(m);
           
            readJTable();
            JOptionPane.showMessageDialog(null, "Alterado com sucesso");
        }
    }//GEN-LAST:event_AlterarActionPerformed

